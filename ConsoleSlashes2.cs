using System;
 public class ConsoleSlashes2
 {
   public static void Main(string[] args)
        {
            Console.BackgroundColor = ConsoleColor.Red;
            Console.ForegroundColor = ConsoleColor.White;
            Console.WriteLine("          ");
            Console.WriteLine("  \\\\\\\\    ");
            Console.WriteLine(" /-----\\  ");
            Console.WriteLine("[|O   O|] ");
            Console.WriteLine(" |  |  |  ");
            Console.WriteLine(" | ___ |  ");
            Console.WriteLine("  \\___/   ");
            Console.WriteLine("          ");
            Console.ResetColor();
        }
  }

